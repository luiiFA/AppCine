    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (tblCategorias.getSelectedRowCount() == 1) {
            int index = tblCategorias.getSelectedRow();// se toma el indice de la filaseleccionada 
            FrmNuevaCategoria winNewCat = new FrmNuevaCategoria(null, true, listaCats, 2, index);
            winNewCat.setTitle("Editar Categoria");
            winNewCat.setVisible(true);
            this.listaCats = winNewCat.listaCats;

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased

        if (txtBuscar.getText().equals("")) {
            formWindowActivated(null);

        } else {

            String titulo[] = {"ID", "Nombre"};
            tblModel = new DefaultTableModel(null, titulo);
            char letra = txtBuscar.getText().charAt(0);

            for (int i = 0; i < listaCats.getTam(); i++) {
                if (listaCats.getVectCategoria()[i].getNombreCat().toLowerCase().charAt(0) == letra) {
                   
                    Object row[] = {listaCats.getVectCategoria()[i].getIdCategoria(), listaCats.getVectCategoria()[i].getNombreCat()};
                    tblModel.addRow(row);

                }

            }

            tblCategorias.setModel(tblModel);
            lblRegistros.setText("Total de registros: " + String.valueOf(tblCategorias.getRowCount()));
        }
    }//GEN-LAST:event_txtBuscarKeyReleased

